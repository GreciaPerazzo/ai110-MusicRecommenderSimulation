class Song:
    def __init__(self, id, title, artist, genre, mood, energy, tempo_bpm, valence, danceability, acousticness):
        self.id = id
        self.title = title
        self.artist = artist
        self.genre = genre
        self.mood = mood
        self.energy = energy
        self.tempo_bpm = tempo_bpm
        self.valence = valence
        self.danceability = danceability
        self.acousticness = acousticness